const int MOD = 1e9+7;

class Solution {
public:
    void sortColors(vector<int>& nums) {    // O(n)
      int l, m, h;
      l = m = 0;
      h = nums.size()-1;
      while(m <= h) {
        if(nums[m] == 0) {
          swap(nums[l], nums[m]);
          l++;
          m++;
        }
        else if(nums[m] == 1) {
          m++;
        }
        else {
          swap(nums[m], nums[h]);
          h--;
        }
      }
    }
};

void solve(Solution sol) {
    string line;
    getline(cin, line); // Read the whole line

    istringstream iss(line); // Create stream from line
    vi nums;
    int x;
    while (iss >> x) {
      nums.pb(x); // Extract numbers one by one
    }
    sol.sortColors(nums);
    for(auto i: nums) {
      cout << i << " ";
    }    
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
    int t = 1;
    // cin >> t;
    Solution obj = Solution();
    while(t--) {
        solve(obj);
        cout << '\n';
    }
    return 0;
}