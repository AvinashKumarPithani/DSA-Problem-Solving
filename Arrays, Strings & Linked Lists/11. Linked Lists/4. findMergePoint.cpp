// count approach |a-b|
int findMergeNode(SinglyLinkedListNode* head1, SinglyLinkedListNode* head2) {   // O(m+n)
    int a=0, b=0;
    SinglyLinkedListNode *p = head1;
    while(p) {
        a++;
        p = p->next;
    }
    p = head2;
    while(p) {
        b++;
        p = p->next;
    }
    SinglyLinkedListNode *q;
    int c = 0;
    if(a > b) {
        c = a-b;
        p = head1;
        q = head2;
    }
    else if(b > a) {
        c = b-a;
        p = head2;
        q = head1;
    }
    else {
        p = head1;
        q = head2;
    }
    while(c--) {
        p = p->next;
    }
    while(p != q) {
        p = p->next;
        q = q->next;
    }
    return p->data;
}

