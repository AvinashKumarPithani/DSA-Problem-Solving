void solve() {
    int n;
    cin >> n;
    if(n<2) {
        cout<< "no";
        return;
    }
    for(int i=2; i*i<=n; i++){
        if(n%i == 0){
            cout << "no";
            return;
        }
    }
    cout << "yes";
}

